
marks1 = [25,30,40,15]
marks2= [17,24]
marks_all = marks1 + marks2
print(marks_all) # [25, 30, 40, 15, 17, 24]
# slicing -> indexing
print(marks_all[0:2]) # [25,30]
print(marks_all[:2]) # [25,30]
print(marks_all[4:6]) # [17,24]
print(marks_all[4:]) # [17,24]
print(marks_all[2:]) # [40, 15, 17, 24]
print(marks_all[:]) # [25, 30, 40, 15, 17, 24]
print(marks_all[::2]) # [25, 40, 17]
print(marks_all[-1:]) # [24]







